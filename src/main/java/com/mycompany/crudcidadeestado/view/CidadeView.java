    private void atualizarTabela() {
    CidadeDAO dao = new CidadeDAO();
    List<Cidade> cidades = dao.listar();

    DefaultTableModel modelo = (DefaultTableModel) tblCidade.getModel();
    modelo.setRowCount(0);

    for (Cidade c : cidades) {
        EstadoDAO estadoDAO = new EstadoDAO();
        Estado estado = estadoDAO.buscarPorId(c.getIdEstado());
        modelo.addRow(new Object[]{
            c.getId(),
            c.getNome(),
            estado != null ? estado.getNome() : "Desconhecido"
        });
    }
}
    
private void preencherComboEstados() {
    cbEstado.removeAllItems(); 
    EstadoDAO estadoDAO = new EstadoDAO();
    List<Estado> estados = estadoDAO.listar(); 

    for (Estado estado : estados) {
        cbEstado.addItem(estado); 
    }
}



    
