    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Cidade cidade = new Cidade();
    cidade.setNome(txtNome.getText());

    Estado estadoSelecionado = (Estado) cbEstado.getSelectedItem();
    if (estadoSelecionado != null) {
        cidade.setIdEstado(estadoSelecionado.getId());
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um estado.");
        return;
    }

    new CidadeDAO().salvar(cidade);
    JOptionPane.showMessageDialog(this, "Cidade salva com sucesso.");
    atualizarTabela();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int row = tblCidade.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma cidade.");
        return;
    }

    Long id = (Long) tblCidade.getValueAt(row, 0);
    Cidade cidade = new Cidade();
    cidade.setId(id);
    cidade.setNome(txtNome.getText());

    Estado estadoSelecionado = (Estado) cbEstado.getSelectedItem();
    cidade.setIdEstado(estadoSelecionado.getId());

    new CidadeDAO().atualizar(cidade);
    JOptionPane.showMessageDialog(this, "Cidade atualizada.");
    atualizarTabela();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int row = tblCidade.getSelectedRow();
    if (row == -1) {
        JOptionPane.showMessageDialog(this, "Selecione uma cidade.");
        return;
    }

    Long id = (Long) tblCidade.getValueAt(row, 0);
    new CidadeDAO().deletar(id);
    JOptionPane.showMessageDialog(this, "Cidade excluÃ­da.");
    atualizarTabela();
    }//GEN-LAST:event_btnExcluirActionPerformed

