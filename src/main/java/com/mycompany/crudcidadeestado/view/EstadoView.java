    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
                                             
        Estado estado = new Estado();
        estado.setNome(txtNome.getText());
        estado.setUf(txtUF.getText());

        EstadoDAO estadoDAO = new EstadoDAO();
        estadoDAO.salvar(estado);

        JOptionPane.showMessageDialog(this, "Estado salvo com sucesso!");
        
        carregarEstados();

    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int row = tblEstados.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um estado para editar.");
            return;
    }

        Long id = (Long) tblEstados.getValueAt(row, 0);
        Estado estado = new Estado();
        estado.setId(id);
        estado.setNome(txtNome.getText());
        estado.setUf(txtUF.getText());

        EstadoDAO estadoDAO = new EstadoDAO();
        estadoDAO.atualizar(estado);

        JOptionPane.showMessageDialog(this, "Estado atualizado com sucesso!");
        
        carregarEstados();
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int row = tblEstados.getSelectedRow();
        if (row == -1) {
         JOptionPane.showMessageDialog(this, "Selecione um estado para excluir.");
            return;
    }

        Long id = (Long) tblEstados.getValueAt(row, 0);
        EstadoDAO estadoDAO = new EstadoDAO();
        estadoDAO.excluir(id);
        
        carregarEstados();

    JOptionPane.showMessageDialog(this, "Estado exclu√≠do com sucesso!");

    }//GEN-LAST:event_btnExcluirActionPerformed

